fun main() {
    val nilai = 60

    if (nilai > 100 || nilai < 0) {
        println("Error")
    } else if (nilai > 90) {
        println("Cool")
    } else if (nilai > 70) {
        println("Mantap")
    } else if (nilai > 60) {
        println("Lumayan")
    } else {
        println("Coba lagi, semangat.")
    }

}