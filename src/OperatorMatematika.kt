fun main() {
    val resultInt = 10 + 5
    val resultDouble = 10.0 + 5.0 / 2.0

    println(resultInt)
    println(resultDouble)

    // Augmented Assigment
    var total = 0
    val barang1 = 10
    total += barang1

    val barang2 = 20
    total += barang2

    val barang3 = 30
    total += barang3

    println(total)

    // Unary Operator
    // ++
    total++
    println(total)

    // --
    total--
    println(total)

    // !
    val bool = true
    println(!bool)

}